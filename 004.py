# 常用数学函数
# python默认提供了丰富的数学函数以降低编程实现的难度，常用的如下实例

import math
# 求绝对值：abs(x)
print("abs(-10)=", abs(-10))
# 向上取整：ceil(x)
print("ceil(3.1415926)=", math.ceil(3.1415926))
# 向下取整：floor(x)
print("floor(2.345)=", math.floor(2.345))
# 四舍五入：round(x)
print("round(3.4567)=", round(3.4567))
# 乘方运算：pow(x,y), x的y次方
print("pow(3, 4)=", pow(3, 4))
# 求平均根：sqrt(x)
print("sqrt(155)=", math.sqrt(155))